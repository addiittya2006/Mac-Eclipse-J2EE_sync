package  mypack;
public class   Rect
    {
          private int length;
          private int breadth;
           public  Rect()   {}
            public  Rect ( int    l, int  b  )    {  length   =l; breadth   =b;  }

         public  String toString()
              {
               return (  length    +  "   "    +  breadth    );
              }
    }